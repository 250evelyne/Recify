import Foundation

enum DietaryRestriction: String, CaseIterable {
    case vegan = "Vegan"
    case vegetarian = "Vegetarian"
    case glutenFree = "Gluten Free"
    case dairyFree = "Dairy Free"
    case keto = "Keto"
    case nutFree = "Nut-Free"
}
